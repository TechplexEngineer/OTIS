OTIS (Online Team Information System) is a project that came about as the team began to grow exponentially.<br>

In the summer between my Junior and Senior years, I learned how to write PHP
(dynamic web code) and how interact with MySQL databases. I realized that with a
web database application we could provide a means of Information transparency
and communication among team members. I created a system which is currently
being used to:
<div style="margin-left: 15px;">
    <ul>
	<li>Mass text-message the team</li>
	<li>Keep up-to-date emergency contact information</li>
	<li>Generate profiles on the team website</li>
	<li>Allow team members to create blog entries</li>
	<li>Manage the instituted team fundraising hour requirements that must be met
	before the member will get his/her way paid to the regional competition.
	These hours are tracked in a Google spreadsheet, from which their hours
	are pulled and populated on their dashboards so they can see their current progress.</li>
    </ul>
</div>
<br>
OTIS empowers team members to manage themselves and their own information.